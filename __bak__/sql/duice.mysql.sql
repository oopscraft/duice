CREATE TABLE EMP
(
   EMPNO int PRIMARY KEY NOT NULL,
   ENAME varchar(20) DEFAULT NULL,
   JOB varchar(20) DEFAULT NULL,
   MGR smallint DEFAULT NULL,
   HIREDATE date DEFAULT NULL,
   SAL decimal(7,2) DEFAULT NULL,
   COMM decimal(7,2) DEFAULT NULL,
   DEPTNO int DEFAULT NULL
)
;
ALTER TABLE EMP
ADD CONSTRAINT FK_EMP_DEPT
FOREIGN KEY (DEPTNO)
REFERENCES DEPT(DEPTNO)
;
CREATE UNIQUE INDEX PRIMARY ON EMP(EMPNO)
;
CREATE INDEX IDX_EMP ON EMP(DEPTNO)
;

CREATE TABLE DEPT
(
   DEPTNO int PRIMARY KEY NOT NULL,
   DNAME varchar(20) DEFAULT NULL,
   LOC varchar(20) DEFAULT NULL
)
;
CREATE UNIQUE INDEX PRIMARY ON DEPT(DEPTNO)
;

CREATE TABLE BONUS
(
   ENAME varchar(10) DEFAULT NULL,
   JOB varchar(9) DEFAULT NULL,
   SAL decimal(10,0) DEFAULT NULL,
   COMM decimal(10,0) DEFAULT NULL
)
;

CREATE TABLE SALGRADE
(
   GRADE decimal(10,0) DEFAULT NULL,
   LOSAL decimal(10,0) DEFAULT NULL,
   HISAL decimal(10,0) DEFAULT NULL
)
;

